-- =============================================================================
-- SECTION 6: approve_swap_request RPC (2.25)
-- Atomically swaps teacher_id between two slots in a single transaction.
-- =============================================================================

CREATE OR REPLACE FUNCTION approve_swap_request(swap_id UUID)
RETURNS VOID AS $$
DECLARE
  v_requesting_slot_id    UUID;
  v_target_slot_id        UUID;
  v_requesting_teacher_id UUID;
  v_target_teacher_id     UUID;
  v_status                TEXT;
BEGIN
  -- Lock and fetch the swap request
  SELECT
    requesting_slot_id,
    target_slot_id,
    requesting_teacher_id,
    target_teacher_id,
    status
  INTO
    v_requesting_slot_id,
    v_target_slot_id,
    v_requesting_teacher_id,
    v_target_teacher_id,
    v_status
  FROM swap_requests
  WHERE id = swap_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Swap request % not found', swap_id;
  END IF;

  IF v_status <> 'pending' THEN
    RAISE EXCEPTION 'Swap request % is not pending (current status: %)', swap_id, v_status;
  END IF;

  -- Swap teacher_id on the requesting slot
  UPDATE slots
  SET teacher_id = v_target_teacher_id
  WHERE id = v_requesting_slot_id;

  -- Swap teacher_id on the target slot
  UPDATE slots
  SET teacher_id = v_requesting_teacher_id
  WHERE id = v_target_slot_id;

  -- Mark the swap request as approved
  UPDATE swap_requests
  SET status = 'approved'
  WHERE id = swap_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


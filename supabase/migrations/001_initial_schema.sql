-- SECTION 7: active_period_mutex TRIGGER (2.26)
-- Ensures only one academic_periods.is_active = true at a time.
-- When a period is activated, all others are set to false.
-- =============================================================================

CREATE OR REPLACE FUNCTION enforce_active_period_mutex()
RETURNS TRIGGER AS $$
BEGIN
  -- Only act when is_active is being set to true
  IF NEW.is_active = true THEN
    UPDATE academic_periods
    SET is_active = false
    WHERE id <> NEW.id
      AND is_active = true;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS active_period_mutex ON academic_periods;
CREATE TRIGGER active_period_mutex
  BEFORE INSERT OR UPDATE OF is_active ON academic_periods
  FOR EACH ROW EXECUTE FUNCTION enforce_active_period_mutex();

